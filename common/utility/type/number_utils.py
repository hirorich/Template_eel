# ==================================================
# 数値型(int型, float型)共通部品
# ==================================================

# num1 > num2 の場合True
def is_greater(num1, num2):
    
    return float(num1) > float(num2)

# num1 >= num2 の場合True
def is_greater_or_equal(num1, num2):
    
    return float(num1) >= float(num2)

# num1 == num2 の場合True
def is_equal(num1, num2):
    
    return float(num1) == float(num2)

# num1 < num2 の場合True
def is_less(num1, num2):
    
    return float(num1) < float(num2)

# num1 <= num2 の場合True
def is_less_or_equal(num1, num2):
    
    return float(num1) <= float(num2)

